# –≤ –ø–æ—Ä—è–¥–∫–µ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—è —Å—Ç–µ–ø–µ–Ω–∏ –¥–æ—Å—Ç–æ–≤–µ—Ä–Ω–æ—Å—Ç–∏, –∞ –ø—Ä–∏ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–∏ —Å—Ç–µ–ø–µ–Ω–µ–π –¥–æ—Å—Ç–æ–≤–µ—Ä–Ω–æ—Å—Ç–∏ ‚Äî –≤ –ª–µ–∫—Å–∏–∫–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–º –ø–æ—Ä—è–¥–∫–µ —Å–∞–º–∏—Ö –Ω–æ–≤–æ—Å—Ç–µ–π.

import sys

data = sys.stdin.readlines()
category = data[-1].strip()
news = data[:-1]
filtered_news = []

for row in news:
    parts = row.rsplit('/', maxsplit=2)
    news_text, news_category, rating = map(str.strip, parts)
    if news_category == category:
        filtered_news.append((news_text, float(rating)))

filtered_news.sort(key=lambda x: (x[1], x[0]))
for news_text, _ in filtered_news:
    print(news_text)


