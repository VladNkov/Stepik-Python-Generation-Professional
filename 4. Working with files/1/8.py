# DESC, –µ—Å–ª–∏ –¥–∞—Ç—ã –≤ –≤–≤–µ–¥–µ–Ω–Ω–æ–π –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω—ã —Å—Ç—Ä–æ–≥–æ –≤ –ø–æ—Ä—è–¥–∫–µ —É–±—ã–≤–∞–Ω–∏—è
# MIX, –µ—Å–ª–∏ –¥–∞—Ç—ã –≤ –≤–≤–µ–¥–µ–Ω–Ω–æ–π –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω—ã –Ω–∏ –≤ –ø–æ—Ä—è–¥–∫–µ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—è, –Ω–∏ –≤ –ø–æ—Ä—è–¥–∫–µ —É–±—ã–≤–∞–Ω–∏—è
# –ü–∞—Ä–∞–º–µ—Ç—Ä—ã ASC –∏ DESC –∏—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è –≤ —è–∑—ã–∫–µ SQL –¥–ª—è —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∏ –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é –∏ –ø–æ —É–±—ã–≤–∞–Ω–∏—é —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ.

import sys
from datetime import datetime

temp = sys.stdin.read().split()
date_list = [datetime.strptime(date, '%d.%m.%Y').date() for date in temp]

asc_temp = sorted(date_list)
desc_temp = sorted(date_list, reverse=True)

if date_list == asc_temp and len(date_list) == len(set(date_list)):
    print('ASC')
if date_list == desc_temp and len(date_list) == len(set(date_list)):
    print('DESC')
if date_list != asc_temp and date_list != desc_temp or len(date_list) != len(set(date_list)):
    print('MIX')



